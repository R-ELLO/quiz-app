//1.Which element is the most electronegative on the periodic table?
// Magnesium
// Fluorine
// Argon
// Barium

//The answer is: Flourine
/* Fluorine is the most electronegative element because it has 5 electrons in its 2p shell. 
The optimal electron configuration of the 2p orbital contains 6 electrons, 
so since fluorine is so close to ideal electron configuration, the electrons are held very tightly to the nucleus.*/

